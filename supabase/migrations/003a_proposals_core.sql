  ON proposals FOR UPDATE
  USING (
    (SELECT role FROM profiles WHERE id = auth.uid()) IN ('founder')
    OR (
      (SELECT role FROM profiles WHERE id = auth.uid()) = 'sales_engineer'
      AND status NOT IN ('accepted', 'rejected')
      -- Cannot edit accepted or rejected proposals.
    )
  );

