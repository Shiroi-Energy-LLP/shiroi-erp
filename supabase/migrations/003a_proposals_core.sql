  'supply',       -- Equipment supply: 5% GST (HSN 8541)
  'works_contract' -- Installation works contract: 18% GST
);

CREATE TYPE scope_owner AS ENUM (
  'shiroi',
  'client',
  'builder',
  'excluded'
