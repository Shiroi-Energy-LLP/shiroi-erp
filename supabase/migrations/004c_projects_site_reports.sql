-- 1. daily_site_reports
-- Written by site supervisor every day from mobile app.
-- Offline-first: written to WatermelonDB first, synced later.
-- Tier 1 within 48h — freely editable.
-- Tier 2 after 48h — correction by new record required.
-- ------------------------------------------------------------
CREATE TABLE daily_site_reports (
  id                    UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  -- UUID generated on device. Enables offline creation.

  project_id            UUID NOT NULL REFERENCES projects(id) ON DELETE RESTRICT,
  milestone_id          UUID REFERENCES project_milestones(id),
  submitted_by          UUID NOT NULL REFERENCES employees(id),

  report_date           DATE NOT NULL,
  -- 'YYYY-MM-DD'. Stored as DATE not TIMESTAMPTZ.
  -- Reason: "2025-03-20" means the same date everywhere.

  -- Workforce
  workers_count         INT NOT NULL DEFAULT 0,
  supervisors_count     INT NOT NULL DEFAULT 0,

  -- Weather
  weather               TEXT NOT NULL DEFAULT 'sunny' CHECK (weather IN (
    'sunny', 'partly_cloudy', 'cloudy', 'rainy', 'stormy'
  )),
  weather_delay         BOOLEAN NOT NULL DEFAULT FALSE,
  weather_delay_hours   NUMERIC(4,1),

  -- Work done today (pre-populated from completion components)
  panels_installed_today    INT NOT NULL DEFAULT 0,
  panels_installed_cumulative INT NOT NULL DEFAULT 0,
  -- Running total. Computed from all reports for this project.

  work_description      TEXT NOT NULL,
  -- Voice-to-text primary input on mobile.

  -- Milestone progress (objective facts only)
  structure_progress    TEXT CHECK (structure_progress IN (
    'not_started', 'columns_done', 'rails_done',
    'bracing_done', 'complete'
  )),
  electrical_progress   TEXT CHECK (electrical_progress IN (
    'not_started', 'inverter_mounted', 'acdb_done',
    'strings_done', 'ac_cable_done', 'complete'
  )),

  -- Issues
  issues_reported       BOOLEAN NOT NULL DEFAULT FALSE,
  issue_summary         TEXT,
  -- Full issue logged in project_issues table.

  -- Material received today
  materials_received    BOOLEAN NOT NULL DEFAULT FALSE,
  materials_summary     TEXT,

  -- Visitors on site
  pm_visited            BOOLEAN NOT NULL DEFAULT FALSE,
  other_visitors        TEXT,

  -- Correction tracking
  has_correction        BOOLEAN NOT NULL DEFAULT FALSE,
  -- Set to TRUE when a site_report_corrections record is created.
  is_locked             BOOLEAN NOT NULL DEFAULT FALSE,
  locked_at             TIMESTAMPTZ,
  -- Set to TRUE after 48h. Trigger runs nightly.

  -- AI narrative (generated from report data)
  ai_narrative          TEXT,
  -- Generated by Claude API. Plain English summary for PM.
  ai_narrative_generated_at TIMESTAMPTZ,

  -- Offline sync
  sync_status           TEXT NOT NULL DEFAULT 'local_only' CHECK (sync_status IN (
    'local_only', 'syncing', 'synced', 'sync_failed'
  )),
  created_on_device_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  -- Device timestamp at creation. May differ from created_at.

  created_at            TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at            TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

