-- 2. site_report_corrections
-- Tier 2 correction record for locked daily site reports.
-- Created when >48h has passed and a correction is needed.
-- Manager must approve. Original flagged has_correction=TRUE.
-- ------------------------------------------------------------
CREATE TABLE site_report_corrections (
  id                    UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  original_report_id    UUID NOT NULL REFERENCES daily_site_reports(id) ON DELETE RESTRICT,
  project_id            UUID NOT NULL REFERENCES projects(id),
  requested_by          UUID NOT NULL REFERENCES employees(id),
  approved_by           UUID REFERENCES employees(id),

  -- What changed
  field_corrected       TEXT NOT NULL,
  -- Which field was wrong. e.g. 'panels_installed_today'
  original_value        TEXT NOT NULL,
  corrected_value       TEXT NOT NULL,
  correction_reason     TEXT NOT NULL,
  -- Mandatory. Cannot be blank.

  status                TEXT NOT NULL DEFAULT 'pending' CHECK (status IN (
    'pending', 'approved', 'rejected'
  )),
  approved_at           TIMESTAMPTZ,
  rejected_reason       TEXT,

  created_at            TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at            TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TRIGGER site_report_corrections_updated_at
  BEFORE UPDATE ON site_report_corrections
  FOR EACH ROW EXECUTE FUNCTION set_updated_at();

CREATE INDEX idx_src_original  ON site_report_corrections(original_report_id);
CREATE INDEX idx_src_project   ON site_report_corrections(project_id);
CREATE INDEX idx_src_pending   ON site_report_corrections(status)
  WHERE status = 'pending';


-- ------------------------------------------------------------
-- 3. site_photos
-- Photos uploaded from mobile app. Offline-first.
-- Photo gates: certain milestone completions require a photo
-- before the count or checklist item is accepted.
-- ------------------------------------------------------------
CREATE TABLE site_photos (
  id                    UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  -- UUID generated on device.

  project_id            UUID NOT NULL REFERENCES projects(id) ON DELETE RESTRICT,
  daily_report_id       UUID REFERENCES daily_site_reports(id),
  -- NULL for photos not linked to a daily report
  -- (e.g. QC gate photos, survey photos).
  uploaded_by           UUID NOT NULL REFERENCES employees(id),

  photo_type            TEXT NOT NULL CHECK (photo_type IN (
    'progress', 'material_received', 'qc_gate',
    'issue', 'commissioning', 'before_work',
    'after_work', 'survey', 'other'
  )),

  milestone_reference   TEXT,
  -- Which milestone this photo evidences.

  storage_path          TEXT NOT NULL UNIQUE,
  file_name             TEXT NOT NULL,
  file_size_bytes       BIGINT,
  mime_type             TEXT DEFAULT 'image/jpeg',

  -- GPS metadata (from mobile device)
  gps_latitude          NUMERIC(9,6),
  gps_longitude         NUMERIC(9,6),
  gps_accuracy_meters   NUMERIC(6,1),
  captured_at           TIMESTAMPTZ,
  -- Device timestamp when photo was taken.

  -- Photo gate
  is_gate_photo         BOOLEAN NOT NULL DEFAULT FALSE,
  gate_verified         BOOLEAN NOT NULL DEFAULT FALSE,
  gate_verified_by      UUID REFERENCES employees(id),
  gate_verified_at      TIMESTAMPTZ,

  -- Caption
  caption               TEXT,

  -- Offline sync
  sync_status           TEXT NOT NULL DEFAULT 'local_only' CHECK (sync_status IN (
    'local_only', 'syncing', 'synced', 'sync_failed'
  )),

  created_at            TIMESTAMPTZ NOT NULL DEFAULT NOW()
  -- Immutable after upload. No updated_at.
);

